package com.javaproject1;

public class DataTypes {
	public static void main(String [] args) {
		int a= 5;//int data type
		boolean b= true;// boolean DataType.
		char ch='a';//char data type;
		float f= 1.1f;//float data type;
		long l=1213;//long data type;
		double d= 12.73d;//double data type
		
	System.out.println("a is int data type:"+a);
	System.out.println("b is boolean data type:"+b);
	System.out.println("ch is char data type:"+ch);
	System.out.println("f is float data type:"+f);
	System.out.println("l is long data type:"+l);
	System.out.println("d is double data type:"+d);
	
		
		
	}

}
